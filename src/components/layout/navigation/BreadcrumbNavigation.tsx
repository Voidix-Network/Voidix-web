import { useSchema } from '@/hooks/useSchema';
import { ChevronRight, Home } from 'lucide-react';
import React from 'react';
import { Link, useLocation } from 'react-router-dom';

interface BreadcrumbItem {
  label: string;
  href?: string;
  isCurrentPage?: boolean;
}

interface BreadcrumbNavigationProps {
  items?: BreadcrumbItem[];
  className?: string;
}

/**
 * 面包屑导航组件
 * 支持SEO优化的结构化数据和无障碍访问
 */
export const BreadcrumbNavigation: React.FC<BreadcrumbNavigationProps> = ({
  items,
  className = '',
}) => {
  const location = useLocation();
  const { addSchema, removeSchema } = useSchema();

  // 自动生成面包屑（如果没有提供items）
  const generateBreadcrumbs = (): BreadcrumbItem[] => {
    const pathSegments = location.pathname.split('/').filter(Boolean);
    const breadcrumbs: BreadcrumbItem[] = [{ label: '首页', href: '/' }];

    // 路径映射
    const pathMap: Record<string, string> = {
      status: '服务器状态',
      monitor: '监控系统',
      faq: '常见问题',
      'bug-report': 'Bug反馈',
      privacy: '隐私政策',
    };

    let currentPath = '';
    pathSegments.forEach((segment, index) => {
      currentPath += `/${segment}`;
      const isLast = index === pathSegments.length - 1;

      breadcrumbs.push({
        label: pathMap[segment] || segment,
        href: isLast ? undefined : currentPath,
        isCurrentPage: isLast,
      });
    });

    return breadcrumbs;
  };

  const breadcrumbs = items || generateBreadcrumbs();
  const breadcrumbsJSON = JSON.stringify(breadcrumbs); // 为useEffect创建稳定的依赖

  // 生成结构化数据（全局唯一，强化去重）
  React.useEffect(() => {
    if (breadcrumbs.length <= 1) return;

    // 获取正确的域名（强制使用生产域名避免测试URL）
    const getBaseUrl = () => {
      // 始终使用生产域名，避免测试域名出现在结构化数据中
      return 'https://www.voidix.net';
    };

    const baseUrl = getBaseUrl();

    const breadcrumbSchema = {
      '@context': 'https://schema.org',
      '@type': 'BreadcrumbList',
      itemListElement: breadcrumbs.map((item, index) => {
        const listItem: any = {
          '@type': 'ListItem',
          position: index + 1,
          name: item.label,
        };

        // 为每个面包屑项添加item属性，确保URL正确
        if (item.href) {
          listItem.item = {
            '@type': 'Thing',
            '@id': `${baseUrl}${item.href}`,
          };
        } else if (item.isCurrentPage && index > 0) {
          // 当前页面使用当前路径
          listItem.item = {
            '@type': 'Thing',
            '@id': `${baseUrl}${location.pathname}`,
          };
        }

        return listItem;
      }),
    };

    const schemaId = 'breadcrumb-list-schema';
    // 使用SchemaManager设置面包屑结构化数据
    addSchema(schemaId, breadcrumbSchema);

    // 清理函数
    return () => {
      removeSchema(schemaId);
    };
  }, [breadcrumbsJSON, location.pathname, addSchema, removeSchema]); // breadcrumbsJSON是稳定的

  // 不显示面包屑如果只有首页
  if (breadcrumbs.length <= 1) {
    return null;
  }

  return (
    <nav aria-label="面包屑导航" className={`flex items-center space-x-2 text-sm ${className}`}>
      <ol className="flex items-center space-x-2">
        {breadcrumbs.map((item, index) => (
          <li key={index} className="flex items-center space-x-2">
            {/* 分隔符 */}
            {index > 0 && <ChevronRight className="w-4 h-4 text-gray-300" aria-hidden="true" />}

            {/* 面包屑项 */}
            {item.href && !item.isCurrentPage ? (
              <Link
                to={item.href}
                className="text-gray-300 hover:text-white transition-colors flex items-center space-x-1"
              >
                {index === 0 && <Home className="w-4 h-4" />}
                <span>{item.label}</span>
              </Link>
            ) : (
              <span
                className={`${
                  item.isCurrentPage ? 'text-white font-medium' : 'text-gray-300'
                } flex items-center space-x-1`}
                aria-current={item.isCurrentPage ? 'page' : undefined}
              >
                {index === 0 && <Home className="w-4 h-4" />}
                <span>{item.label}</span>
              </span>
            )}
          </li>
        ))}
      </ol>
    </nav>
  );
};

export default BreadcrumbNavigation;
