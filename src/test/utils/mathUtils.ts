/**
 * 基础数学工具函数
 * 提供简单的数学计算功能用于测试框架验证
 */

/**
 * 加法运算
 * @param a - 第一个数字
 * @param b - 第二个数字
 * @returns 两数之和
 */
export const add = (a: number, b: number): number => a + b;

/**
 * 乘法运算
 * @param a - 第一个数字
 * @param b - 第二个数字
 * @returns 两数之积
 */
export const multiply = (a: number, b: number): number => a * b;
